        public void Method_jmyak_chmyak() {


            string connectionString = @"Data Source=DOM\SQLEXPRESS;Initial Catalog=itreheniya;Integrated Security=True;Encrypt=False";

            // Запрос к базе данных для получения значений
            string query = "SELECT Статус_заявки FROM Заявки WHERE Номер_телефона = '" + textBox2.Text+"'";

            // Подключение к базе данных и выполнение запроса
            using (SqlConnection connection = new SqlConnection(connectionString))
            {
                SqlCommand command = new SqlCommand(query, connection);
                connection.Open();

                // Выполнение запроса и чтение данных
                SqlDataReader reader = command.ExecuteReader();
                if (reader.Read())
                {


                   string result = reader["Статус_заявки"].ToString();
                    label5.Text = result;
                    panel4.Visible = true;



                    if (result == "Выполнено") {
                        label2.Visible = true;
                        label2.Text = " Можите забирать заказ";
                    
                    
                    
                    }




                }

                else {

                    MessageBox.Show("Повторите попытку,неверно введённые данные", "WARNING", MessageBoxButtons.OK, MessageBoxIcon.Warning);


                }

                reader.Close();
            }


        }

